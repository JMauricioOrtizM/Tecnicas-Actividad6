    void ListarArticulo(){
        for(int i=0;i<articulo.getTamaño();i++){
        Articulo a= articulo.obtener(i);
        
         tArticulos.setValueAt(a.getId(),i,0);
         tArticulos.setValueAt(a.getCategoria(),i,1);
         tArticulos.setValueAt(a.getCodigo(),i,2);
         tArticulos.setValueAt(a.getNombre(),i,3);
         tArticulos.setValueAt(a.getPrecioVenta(),i,4);
         tArticulos.setValueAt(a.getStock(),i,5);
         tArticulos.setValueAt(a.getDescripcion(),i,6);
         tArticulos.setValueAt(a.getRutaImagen(),i,7);
         tArticulos.setValueAt(a.getEstado(),i,8);
        }
    }

