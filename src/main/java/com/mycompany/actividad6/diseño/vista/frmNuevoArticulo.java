    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
   JFileChooser fc= new JFileChooser();
   
   FileNameExtensionFilter filtro = new FileNameExtensionFilter("JPG & GIF Images", "jpg", "gif","jpeg");    
   fc.setFileFilter(filtro);
       
   int seleccion=fc.showOpenDialog(this);
   if (seleccion==JFileChooser.APPROVE_OPTION){
       File fichero=fc.getSelectedFile();
       //esta es la direccion a grabar en el archivo plano
       this.txtRutaImagen.setText(fichero.getAbsolutePath());
   try(FileReader fr=new FileReader(fichero)){
       ImageIcon ImgIcon=new ImageIcon(fichero.toString());
       Icon icono= new ImageIcon(ImgIcon.getImage().getScaledInstance(jlbImagen.getWidth(), jlbImagen.getHeight(),Image.SCALE_DEFAULT));
       jlbImagen.setIcon(icono);
              
    }catch(IOException e){
       JOptionPane.showMessageDialog(null,"ERROR!!! al abrir..."+ e);
        }
   }

    
        
        
        
    }//GEN-LAST:event_btnAgregarActionPerformed


